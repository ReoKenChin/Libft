#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	int		count;
	char	*str;
	char	*p;

	i = 0;
	count = 0;
	str = (char *)s1;
	p = malloc((ft_strlen(str) + 1) * sizeof(char));
	if (!p)
		return (NULL);
	while (str[i])
	{
		p[i] = str[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
